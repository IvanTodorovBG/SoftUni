x = float(input()) * 100
sum = int(x)
remaining_sum = 0
coins = 0
while sum != 0:
    if sum >= 200:
        sum -= 200
        coins += 1
    if 100 <= sum < 200:
        sum -= 100
        coins += 1
    if 50 <= sum < 100:
        sum -= 50
        coins += 1
    if 20 <= sum < 50:
        sum -= 20
        coins += 1
    if 10 <= sum < 20:
        sum -= 10
        coins += 1
    if 5 <= sum < 10:
        sum -= 5
        coins += 1
    if 2 <= sum < 5:
        sum -= 2
        coins += 1
    if 1 <= sum < 2:
        sum -= 1
        coins += 1
print(coins)
